x = 'This is'
y = 'a sentence'

# Concatenating two string variables
z = x + y 
print(z)

# Concatenating two string variables and a string
z = x + ' ' + y 
print(z)

# Concatenating a string variable, a string, an integer 
# (that has been converted to a string) and a string
z = x + ' ' + str(4) + ' words'
print(z)
