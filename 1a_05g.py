x = ['Jing','Kate','Lisa','Marc'] # Alphabetical order
y = [1,3,4,5,8,9] # Ascending order
z = [5,9,8,1,4,3] # Unsorted list

# Reverses the order of list x
x.reverse() 
print(x)

# Reverses the order of list y
y.reverse() 
print(y)

# Reverses the order of list z, an unsorted list
z.reverse() 
print(z)
