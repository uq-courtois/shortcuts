#### LIST
# Iterator value = element in list
x = [1,2,3,4]

for i in x:
	print(i)

### LIST OF DICTIONARIES
# Iterator value = element in list = dictionary

x = [
	{'Name':'Jamie','Age':19},
	{'Name':'Ella','Age':21},
]

for i in x:
	print(i)

	# We can access a value by adding a key:
for i in x: 
	print(i['Name'])

# STRING
# Iterator value = character in string
x = 'This is a sentence'

for i in x:
	print(i)

