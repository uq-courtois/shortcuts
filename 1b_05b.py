# A list 
# Iterator value = element
x = [1,2,3,4]

for i in x:
	print(i)

# A list of dictionaries
# Iterator value = element = dictionary

x = [
	{'Name':'Jamie','Age':19,},
	{'Name':'Ella','Age':21},
]

for i in x:
	print(i)

# A dictionary
# Iterator value = key
x = {'Name':'Jamie','Age':19,}

for i in x:
	print(i)

# A string
# Iterator value = character in string
x = 'This is a sentence'

for i in x:
	print(i)

